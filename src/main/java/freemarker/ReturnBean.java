package freemarker;

public class ReturnBean {

    private String file;
    private String sha1;

    public ReturnBean() {
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getSha1() {
        return sha1;
    }

    public void setSha1(String sha1) {
        this.sha1 = sha1;
    }

   
}
